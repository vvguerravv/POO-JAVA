package engtelecom.poo.objetos;

public class Coordenadas {

    private final double x;
    private final double y;

    public Coordenadas(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
